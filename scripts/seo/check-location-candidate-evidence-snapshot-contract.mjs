import { readFileSync } from 'node:fs';
import { resolve } from 'node:path';

const projectRoot = process.cwd();

const files = {
  snapshotContract: 'src/config/geo/location-candidate-evidence-snapshot-contract.ts',
  thresholdPolicy: 'src/config/geo/location-threshold-policy.ts',
  candidateRuntime: 'src/lib/geo/oman-location-candidates.ts',
  candidateGate: 'scripts/seo/check-location-category-candidate-gate.mjs',
  packageJson: 'package.json',
};

const expectedPairs = [
  ["entity: 'governorate'", "dimension: 'category'"],
  ["entity: 'governorate'", "dimension: 'service'"],
  ["entity: 'governorate'", "dimension: 'specialty'"],
  ["entity: 'wilayat'", "dimension: 'category'"],
  ["entity: 'wilayat'", "dimension: 'service'"],
  ["entity: 'wilayat'", "dimension: 'specialty'"],
  ["entity: 'area'", "dimension: 'category'"],
  ["entity: 'area'", "dimension: 'service'"],
  ["entity: 'area'", "dimension: 'specialty'"],
];

function read(relativePath) {
  return readFileSync(resolve(projectRoot, relativePath), 'utf8');
}

function requireTokens(label, source, tokens) {
  const missing = tokens.filter((token) => !source.includes(token));
  if (missing.length > 0) {
    console.error(`${label} is missing required candidate evidence snapshot tokens:`);
    for (const token of missing) {
      console.error(`- ${token}`);
    }
    process.exit(1);
  }
}

function forbidTokens(label, source, tokens) {
  const forbidden = tokens.filter((token) => source.includes(token));
  if (forbidden.length > 0) {
    console.error(`${label} contains forbidden candidate evidence snapshot tokens:`);
    for (const token of forbidden) {
      console.error(`- ${token}`);
    }
    process.exit(1);
  }
}

const snapshotContract = read(files.snapshotContract);
const thresholdPolicy = read(files.thresholdPolicy);
const candidateRuntime = read(files.candidateRuntime);
const candidateGate = read(files.candidateGate);
const packageJson = read(files.packageJson);

requireTokens(files.snapshotContract, snapshotContract, [
  'OMAN_LOCATION_CANDIDATE_EVIDENCE_SNAPSHOT_CONTRACT_VERSION',
  'OmanLocationCandidateEvidenceSnapshotShape',
  'parentHierarchyResolved',
  'publishedProviderCount',
  'verifiedProviderCount',
  'approvedEvidenceEntries',
  'readinessStatus',
  'promotionReviewApproved',
  'sourceRefs',
  'reviewedBy',
  'reviewedAt',
  'blockedReasons',
  'parentHierarchyRequired: true',
  'providerCountsRequired: true',
  'evidenceCountsRequired: true',
  'readinessStatusRequired: true',
  'promotionReviewRequired: true',
  'blockedReasonsRequired: true',
  'sourceRefsRequired: true',
  'reviewerRequired: true',
  'snapshotPromotionAllowed: false',
  "status: 'contract-only'",
  'currentSnapshotsAvailable: false',
  'runtimeSnapshotGenerationAllowed: false',
  'databaseAccessAllowed: false',
  'sitemapPromotionAllowed: false',
  'jsonLdAllowed: false',
  'indexPromotionAllowed: false',
  'No candidate evidence snapshot is generated by this contract.',
  'No candidate becomes indexable by this contract.',
]);

for (const [entityToken, dimensionToken] of expectedPairs) {
  requireTokens(files.snapshotContract, snapshotContract, [entityToken, dimensionToken]);
}

forbidTokens(files.snapshotContract, snapshotContract, [
  'snapshotPromotionAllowed: true',
  'currentSnapshotsAvailable: true',
  'runtimeSnapshotGenerationAllowed: true',
  'databaseAccessAllowed: true',
  'sitemapPromotionAllowed: true',
  'jsonLdAllowed: true',
  'indexPromotionAllowed: true',
]);

requireTokens(files.thresholdPolicy, thresholdPolicy, [
  'minimumPublishedProviders',
  'minimumVerifiedProviders',
  'minimumApprovedEvidenceEntries',
  'promotionRequiresParentHierarchy: true',
  'promotionRequiresApprovedEvidence: true',
]);

requireTokens(files.candidateRuntime, candidateRuntime, [
  'parentHierarchyResolved',
  'providerThresholdMet',
  'approvedEvidenceComplete',
  'readinessGatesComplete',
  'humanReviewComplete',
  'promotionPrApproved',
  'blockedReasons',
  "status: 'blocked'",
]);

requireTokens(files.candidateGate, candidateGate, [
  'minimumPublishedProviders',
  'minimumApprovedEvidenceEntries',
  'registryEnabled: false',
  'promotionAllowed: false',
]);

requireTokens(files.packageJson, packageJson, [
  'seo:location-candidate-evidence-snapshot:validate',
  'check-location-candidate-evidence-snapshot-contract.mjs',
]);

console.log('Location candidate evidence snapshot contract validation passed.');
